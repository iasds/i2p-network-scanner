def scan_streaming(datadirs: list[str], outpath: str):
    total_files = 0
    parsed = 0
    cn_count = 0
    seen = set()  # track seen ident_hashes for CN
    
    with open(outpath, 'w') as out:
        for d in datadirs:
            files = list(Path(d).glob("r*/routerInfo-*.dat"))
            total_files += len(files)
            print(f"{d}: {len(files)} files", flush=True)
            
            for f in files:
                try:
                    raw = f.read_bytes()
                    ri = parse_router_info(raw)
                    if ri is None:
                        continue
                    parsed += 1
                    
                    # Check if any address is CN
                    cn_addrs = [a for a in ri['addresses'] if is_cn_ip(a['ip'])]
                    if cn_addrs:
                        ri['addresses'] = cn_addrs
                        if ri['ident_hash'] not in seen:
                            seen.add(ri['ident_hash'])
                            cn_count += 1
                            out.write(json.dumps(ri) + '\n')
                except Exception:
                    pass
                
                if parsed % 5000 == 0:
                    print(f"  parsed={parsed} cn={cn_count} unique_cn={len(seen)}", flush=True)
    
    print(f"\nDone: files={total_files} parsed={parsed} cn_routers={cn_count} unique_cn={len(seen)}", flush=True)
    return cn_count

